# Прогрессивная система скидок
shop <- function(x){
  if(x >= 100 && x < 1000){
    x = x - x*12.5/100
    print(x)
  }
  else if(x >= 1000){
    x = x - x*20/100
    print(x)
  }
  else{
    print(c(x, "Нет скидки")) 
  }
}
# Сумма покупок 20$
shop(20)
# Сумма покупок 200$
shop(200)
# Сумма покупок 2000$
shop(2000)